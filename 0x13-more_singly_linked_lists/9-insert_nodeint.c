#include "lists.h"

/**
 * insert_nodeint_at_index - insert a new node to a linked list at a given position
 * @head:A pointer to the address of the linked list
 * @n: An intriger for the new node to contain
 * @idx: index of the linked list - new node should be added
 * Return: if the function fails - NULL -the element of the new node
 */
listint_t *insert_nodeint_at_index(listint_t **head, unsigned int idx, int n)
{
	unsigned int i;
	listint_t *new;
	listint_t *temp = *head;

	new = malloc(sizeof(listint_t));
	if (!new || !head)
		return (NULL);

	new->n = n;
	new->next = NULL;

	if (idx == 0)
	{
		new->next = *head;
		*head = new;
		return (new);
	}

	for (i = 0; temp && i < idx; i++)
	{
		if (i == idx - 1)
		{
			new->next = temp->next;
			temp->next = new;
			return (new);
		}
		else
			temp = temp->next;
	}

	return (NULL);
}
